//choose the colors for everything
const colorKey = {
    0: '#000',
    1: '#333',
    2: '#0f0',
    3: '#00f',
    4: '#f00',
    sensor: false ? '#00f6' : '#0000',
    background: '#151515'
}

/**level block key
 * 0: empty
 * 1: solid
 * 2: player / start
 * 3: end
 * 4: death
 */

//keep track of which level is current
let levelIndex = 0

//used for matter calculations only
const blockSize = 50

//what size to make the floor sensors as a ratio to blockSize
const floorSensorSize = .1

//some player parms
const playerBlockSize = .75
const jumpStrength = .05
const airSideStrength = .001
const floorSlideStrength = .005

const coyoteTime = 250 //a quarter second of time you can jump after you go off a ledge

//hold the player
let player

//hold the end
let end

//also hold the floors out here
let floorSensors = []

//hold the bad blocks
let deathBlocks = []

const loadLevel = (level) => {
    //clear the old world
    Matter.World.clear(engine.world)
    Matter.Engine.clear(engine)

    //reset things
    player = undefined
    end = undefined
    floorSensors = []
    deathBlocks = []

    //set the renders size to fit the lever
    const width = level[0].length * blockSize
    const height = level.length * blockSize

    Matter.Render.lookAt(render, {
        min: { x: 0, y: 0 },
        max: { x: width, y: height }
    });

    //create the walls for the level
    Matter.World.add(world, Matter.Bodies.rectangle(width / 2, -blockSize / 2, width, blockSize, { isStatic: true })) //top
    Matter.World.add(world, Matter.Bodies.rectangle(width / 2, height + blockSize / 2, width, blockSize, { isStatic: true })) //bottom
    Matter.World.add(world, Matter.Bodies.rectangle(-blockSize / 2, height / 2, blockSize, height, { isStatic: true })) //left
    Matter.World.add(world, Matter.Bodies.rectangle(width + blockSize / 2, height / 2, blockSize, height, { isStatic: true })) //right

    //and the floor sensor
    let sensor = Matter.Bodies.rectangle(
        width / 2,
        height,
        width, blockSize * floorSensorSize,
        { isStatic: true, label: 'floorSensor', render: { fillStyle: colorKey.sensor } })
    floorSensors.push(sensor)
    Matter.World.add(world, sensor)


    //create the level
    for (let x = 0; x < level[0].length; x++)
        for (let y = 0; y < level.length; y++) {
            const blockType = level[y][x]

            //create a solid block
            if (blockType == 1) {

                //create the block
                Matter.World.add(world, Matter.Bodies.rectangle(
                    x * blockSize + blockSize / 2,
                    y * blockSize + blockSize / 2,
                    blockSize, blockSize,
                    { isStatic: true, render: { fillStyle: colorKey[blockType] } }))

                //check if there is a solid block right above, if so, don't make a sensor
                if (level?.[y - 1]?.[x] != 1) {

                    //create the floor sensor
                    let sensor = Matter.Bodies.rectangle(
                        x * blockSize + blockSize / 2,
                        y * blockSize + blockSize * floorSensorSize / 2,
                        blockSize, blockSize * floorSensorSize,
                        { isStatic: true, label: 'floorSensor', render: { fillStyle: colorKey.sensor } })
                    floorSensors.push(sensor)
                    Matter.World.add(world, sensor)
                }
            }

            //create the player
            if (blockType == 2) {
                player = Matter.Bodies.rectangle(
                    x * blockSize + blockSize * playerBlockSize / 2,
                    y * blockSize + blockSize * playerBlockSize / 2,
                    blockSize * playerBlockSize, blockSize * playerBlockSize,
                    { render: { fillStyle: colorKey[blockType] } }
                )
                Matter.World.add(world, player)
            }

            //create the end
            if (blockType == 3) {
                end = Matter.Bodies.rectangle(
                    x * blockSize + blockSize / 2,
                    y * blockSize + blockSize / 2,
                    blockSize, blockSize,
                    { isStatic: true, render: { fillStyle: colorKey[blockType] } })
                Matter.World.add(world, end)
            }

            //add deathBlocks
            if (blockType == 4) {
                let block = Matter.Bodies.rectangle(
                    x * blockSize + blockSize / 2,
                    y * blockSize + blockSize / 2,
                    blockSize, blockSize,
                    { isStatic: true, render: { fillStyle: colorKey[blockType] } })
                deathBlocks.push(block)
                Matter.World.add(world, block)
            }
        }


}

loadLevel(levels[levelIndex])


let keys = []
let lastOnFloor = 0
setInterval(() => {
    if (player) {

        //check for ends
        if (end && Matter.Query.collides(player, [end]).length > 0)
            if (levelIndex + 1 < levels.length)
                loadLevel(levels[levelIndex++ + 1])
            else {
                alert('You won!')
                player = undefined
                location.reload()
            }

        //handle deathBlocks
        if (deathBlocks.length > 0 && Matter.Query.collides(player, deathBlocks).length > 0)
            loadLevel(levels[levelIndex])


        //move the player
        const onFloor = Matter.Query.collides(player, floorSensors).length > 0 && player.velocity.y > -.1
        if (onFloor) lastOnFloor = Date.now()
        if (keys.includes('w') && Date.now() - lastOnFloor <= coyoteTime) {
            Matter.Body.applyForce(player, { x: player.position.x, y: player.position.y + blockSize * playerBlockSize / 2 }, { x: 0, y: -jumpStrength })
            lastOnFloor = 0
        }
        if (keys.includes('a'))
            Matter.Body.applyForce(player, { x: player.position.x, y: player.position.y - blockSize * playerBlockSize / 2 }, { x: onFloor ? -floorSlideStrength : -airSideStrength, y: 0 })
        if (keys.includes('d'))
            Matter.Body.applyForce(player, { x: player.position.x, y: player.position.y - blockSize * playerBlockSize / 2 }, { x: onFloor ? floorSlideStrength : airSideStrength, y: 0 })
    }
}, 1000 / 100)

document.addEventListener('keyup', event => {
    if (['w', 'a', 's', 'd'].includes(event.key) && keys.includes(event.key))
        keys.splice(keys.indexOf(event.key), 1)
})

document.addEventListener('keydown', event => {
    if (['w', 'a', 's', 'd'].includes(event.key) && !keys.includes(event.key))
        keys.push(event.key)
})

document.addEventListener('keypress', event => {
    if (event.key == '`') {
        if (levelIndex + 1 < levels.length)
            loadLevel(levels[levelIndex++ + 1])
        else {
            alert('You won!')
            player = undefined
            location.reload()
        }
    }
})

// Run the engine
Matter.Runner.run(engine)

