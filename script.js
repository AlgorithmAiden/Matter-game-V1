const loadLevel = (level, first) => {
    //clear the old world
    Matter.World.clear(engine.world)
    Matter.Engine.clear(engine)

    //reset things
    player = undefined
    end = undefined
    floorSensors = []
    deathBlocks = []

    //set the renders size to fit the level
    width = level[0].length * blockSize
    height = level.length * blockSize

    if (first) {
        //run a camera animation
        isCameraRunningCutscene = true
        camera.scale = 0
        camera.x = width / 2
        camera.y = height / 2
        const part1Handle = setInterval(() => {
            camera.scale = camera.scale + (Math.max(width / blockSize, height / blockSize) - camera.scale) / 35
        }, 1000 / 100)
        setTimeout(() => {
            clearInterval(part1Handle)
            isCameraRunningCutscene = false
        }, 2000)
    }

    //create the walls for the level
    Matter.World.add(world, Matter.Bodies.rectangle(width / 2, -blockSize / 2, width, blockSize, { isStatic: true, render: { fillStyle: colorKey[1] } })) //top
    Matter.World.add(world, Matter.Bodies.rectangle(width / 2, height + blockSize / 2, width, blockSize, { isStatic: true, render: { fillStyle: colorKey[1] } })) //bottom
    Matter.World.add(world, Matter.Bodies.rectangle(-blockSize / 2, height / 2, blockSize, height, { isStatic: true, render: { fillStyle: colorKey[1] } })) //left
    Matter.World.add(world, Matter.Bodies.rectangle(width + blockSize / 2, height / 2, blockSize, height, { isStatic: true, render: { fillStyle: colorKey[1] } })) //right

    //and the floor sensor
    let sensor = Matter.Bodies.rectangle(
        width / 2,
        height,
        width, blockSize * floorSensorSize,
        { isStatic: true, label: 'floorSensor', render: { fillStyle: colorKey.sensor } })
    floorSensors.push(sensor)
    Matter.World.add(world, sensor)


    //create the level
    for (let x = 0; x < level[0].length; x++)
        for (let y = 0; y < level.length; y++) {
            const blockType = level[y][x]

            //create a solid block
            if (blockType == 1) {

                //create the block
                Matter.World.add(world, Matter.Bodies.rectangle(
                    x * blockSize + blockSize / 2,
                    y * blockSize + blockSize / 2,
                    blockSize, blockSize,
                    { isStatic: true, render: { fillStyle: colorKey[blockType] } }))

                //check if there is a solid block right above, if so, don't make a sensor
                if (level?.[y - 1]?.[x] != 1) {

                    //create the floor sensor
                    let sensor = Matter.Bodies.rectangle(
                        x * blockSize + blockSize / 2,
                        y * blockSize + blockSize * floorSensorSize / 2,
                        blockSize, blockSize * floorSensorSize,
                        { isStatic: true, label: 'floorSensor', render: { fillStyle: colorKey.sensor } })
                    floorSensors.push(sensor)
                    Matter.World.add(world, sensor)
                }
            }

            //create the player
            if (blockType == 2) {
                player = Matter.Bodies.rectangle(
                    x * blockSize + blockSize * playerBlockSize / 2,
                    y * blockSize + blockSize * playerBlockSize / 2,
                    blockSize * playerBlockSize, blockSize * playerBlockSize,
                    { render: { fillStyle: colorKey[blockType] } }
                )
                Matter.World.add(world, player)
            }

            //create the end
            if (blockType == 3) {
                end = Matter.Bodies.rectangle(
                    x * blockSize + blockSize / 2,
                    y * blockSize + blockSize / 2,
                    blockSize, blockSize,
                    { isStatic: true, render: { fillStyle: colorKey[blockType] } })
                Matter.World.add(world, end)
            }

            //add deathBlocks
            if (blockType == 4) {
                let block = Matter.Bodies.rectangle(
                    x * blockSize + blockSize / 2,
                    y * blockSize + blockSize / 2,
                    blockSize, blockSize,
                    { isStatic: true, render: { fillStyle: colorKey[blockType] } })
                deathBlocks.push(block)
                Matter.World.add(world, block)
            }
        }


}

